// ─── ARTICLE PAGE (aktualnosc.html) ─────────────────────────────────
function renderNewsArticle() {
  const wrap = document.getElementById('articleWrap');
  if (!wrap) return;

  const id = parseInt(new URLSearchParams(window.location.search).get('id'), 10);
  const news = NEWS.find(n => n.id === id);
  const idx  = NEWS.findIndex(n => n.id === id);

  if (!news) {
    wrap.innerHTML = `
      <div class="not-found">
        <h1>Nie znaleziono aktualności</h1>
        <p>Sprawdź czy adres URL jest poprawny.</p>
        <a href="aktualnosci.html" style="color:var(--orange);font-family:var(--font-h);letter-spacing:2px;text-transform:uppercase;margin-top:8px;">← Wszystkie aktualności</a>
      </div>`;
    return;
  }

  document.title = `${news.title} — Ekipa Zimnego Lecha`;

  const prev = NEWS[idx + 1];
  const next = NEWS[idx - 1];

  const navHTML = `
    <div class="article-nav">
      ${prev ? `<a href="aktualnosc.html?id=${prev.id}" class="article-nav-btn">← ${prev.title}</a>` : '<span></span>'}
      ${next ? `<a href="aktualnosc.html?id=${next.id}" class="article-nav-btn article-nav-btn--right">${next.title} →</a>` : '<span></span>'}
    </div>`;

  const relatedHTML = NEWS.filter(n => n.id !== id).map(n => `
    <a href="aktualnosc.html?id=${n.id}" class="related-card">
      <span class="related-emoji">${n.emoji}</span>
      <div>
        <div class="related-cat">${n.category}</div>
        <div class="related-title">${n.title}</div>
        <div class="related-date">${n.dateDisplay}</div>
      </div>
    </a>`).join('');

  wrap.innerHTML = `
    <section class="article-hero fade-in" ${(news.heroImage || news.image) ? `style="background-image:url('${news.heroImage || news.image}');background-size:cover;background-position:center;"` : ''}>
      ${news.image ? '<div style="position:absolute;inset:0;background:linear-gradient(to right,rgba(13,13,13,.92) 40%,rgba(13,13,13,.6) 100%);"></div>' : ''}
      <div class="article-hero-shape"></div>
      <div class="container">
        <div class="article-breadcrumb">
          <a href="index.html">Strona główna</a> ›
          <a href="aktualnosci.html">Aktualności</a> ›
          <span>${news.category}</span>
        </div>
        <div class="article-meta-top">
          <span class="article-category-badge">${news.category}</span>
          <span class="article-date">📅 ${news.dateDisplay}</span>
          <span class="article-author">✍️ ${news.author}</span>
        </div>
        <h1 class="article-title">${newsNameHTML(news)}</h1>
        <p class="article-lead">${news.excerpt}</p>
      </div>
    </section>

    <section class="article-body-section">
      <div class="container">
        <div class="article-layout">
          <div class="article-content fade-in">
            <div class="article-emoji-block">${news.emoji}</div>
            ${news.content}
            ${navHTML}
          </div>
          <aside class="article-sidebar fade-in">
            <div class="sidebar-card">
              <div class="sidebar-card-title">Szczegóły</div>
              <div class="sidebar-item">
                <div class="sidebar-item-label">Kategoria</div>
                <div class="sidebar-item-value">${news.category}</div>
              </div>
              <div class="sidebar-item">
                <div class="sidebar-item-label">Data</div>
                <div class="sidebar-item-value">${news.dateDisplay}</div>
              </div>
              <div class="sidebar-item">
                <div class="sidebar-item-label">Autor</div>
                <div class="sidebar-item-value">${news.author}</div>
              </div>
            </div>
            <div class="sidebar-card" style="margin-top:24px;">
              <div class="sidebar-card-title">Inne aktualności</div>
              ${relatedHTML}
            </div>
          </aside>
        </div>
      </div>
    </section>
  `;

  setTimeout(() => {
    document.querySelectorAll('.fade-in').forEach((el, i) => {
      setTimeout(() => el.classList.add('visible'), i * 80);
    });
  }, 50);
}

